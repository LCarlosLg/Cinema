//Esta linea codigo permite exraer todos los datos de la tabla hall 
    private void btnSearchHallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchHallActionPerformed
       try{ //Metodo para que intente leer la informaci√≥n.
        String filtro = txtName.getText();
            
            List<Hall> halls = Hall.getAll(filtro);
            DefaultTableModel model= (DefaultTableModel) tblHall.getModel(); //Implementa defaulttablemodel para que se puedan modificar todos los datos dentro de tblTransactions.
            model.setRowCount(0);
            
            for(Hall h : halls){
             Object [] rowData ={h.getId(), h.getNumber(), h.getAvailability()};
             
               model.addRow(rowData);
                
            }
        }catch (Exception ex){
           System.err.println("Ocurrio un error: "+ ex.getMessage());
           
           } 
     
    }//GEN-LAST:event_btnSearchHallActionPerformed
